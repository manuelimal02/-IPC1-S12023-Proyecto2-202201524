    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(usuarioTXT.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Vacío. Debe ingresar un nombre de usuario.", "Ugallery", JOptionPane.WARNING_MESSAGE); 
        }else{
            
            NombreUsuario=usuarioTXT.getText();
            Usuario usuarioExistente = (Usuario)AppState.listaUsuario.find(NombreUsuario);;
            
            if(usuarioExistente!=null){
                BibliotecaJFrame MenuBiblioteca = new BibliotecaJFrame();
                MenuBiblioteca.nombreUsuarioTXT.setText(NombreUsuario);
                MenuBiblioteca.setVisible(true);
                dispose();
                AppState.listaUsuario.ImprimirUsuarios();
                JOptionPane.showMessageDialog(null, "Bienvenido A La Biblioteca De Imágenes", "Ugallery", JOptionPane.INFORMATION_MESSAGE);
            }else{
                
                JOptionPane.showMessageDialog(null, "Usuario Creado Por Primera Vez", "Ugallery", JOptionPane.INFORMATION_MESSAGE);
                
                Usuario nuevoUsuario = new Usuario();
                Categoria nuevaCategoria = new Categoria();
                nuevaCategoria.setNombreCategoria("General");
                nuevoUsuario.setNombreUsuario(NombreUsuario);
                nuevoUsuario.listaCategoria.add(nuevaCategoria);
                AppState.listaUsuario.add(nuevoUsuario);
                
                BibliotecaJFrame MenuBiblioteca = new BibliotecaJFrame();
                MenuBiblioteca.nombreUsuarioTXT.setText(NombreUsuario);
                MenuBiblioteca.setVisible(true);
                dispose();
                
                AppState.listaUsuario.ImprimirUsuarios();
                JOptionPane.showMessageDialog(null, "Bienvenido A La Biblioteca De Imágenes", "Ugallery", JOptionPane.INFORMATION_MESSAGE);
                
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

