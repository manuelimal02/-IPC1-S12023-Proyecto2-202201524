    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Usuario usuarioActual = (Usuario) AppState.listaUsuario.find(NombreUsuario);
        String nombreCategoria=nombreCategoriaTXT.getText();
        if(nombreCategoriaTXT.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Vacío. Debe ingresar el nombre de la categoría.", "Ugallery", JOptionPane.ERROR_MESSAGE);
        }else{
            boolean existe=false;
            for (Categoria categoria : usuarioActual.getListaCategoria()) {
                if(categoria.getNombreCategoria().equals(nombreCategoriaTXT.getText())) {
                    existe = true;
                    break;
                }
            }
            if(existe){
                JOptionPane.showMessageDialog(null, "Esta Categoría Ya Existe.", "Ugallery", JOptionPane.WARNING_MESSAGE);
            }else{
                Categoria nuevaCategoria = new Categoria();
                nuevaCategoria.setNombreCategoria(nombreCategoria);
                usuarioActual.agregarCategoria(nuevaCategoria);
                DefaultListModel<String> modelo2 = (DefaultListModel<String>)listaCategoriaLT.getModel();
                modelo2.addElement(nuevaCategoria.getNombreCategoria());
                listaCategoriaLT.setModel(modelo2);
                JOptionPane.showMessageDialog(null, "Categoría Agregada Correctamente.", "Ugallery", JOptionPane.INFORMATION_MESSAGE);
                
                usuarioActual.ImprimirCategorias();
                nombreCategoriaTXT.setText(null);
                nombreFotoTXT.setText(null);
                fotoLBL.setIcon(null);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Usuario usuarioActual = (Usuario) AppState.listaUsuario.find(NombreUsuario);
        if(nombreCategoriaTXT.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo Vacío. Debe ingresar el nombre de la categoría.", "Ugallery", JOptionPane.ERROR_MESSAGE);
        }else if(nombreCategoriaTXT.getText().equalsIgnoreCase("General")){
             JOptionPane.showMessageDialog(null, "No es posible eliminar la categoría “General”.", "Ugallery", JOptionPane.ERROR_MESSAGE);
        }else{
            boolean existe=false;
            for (Categoria categoria : usuarioActual.getListaCategoria()) {
                if (categoria.getNombreCategoria().equals(nombreCategoriaTXT.getText())) {
                    existe=true;
                    usuarioActual.getListaCategoria().remove(categoria);
                    JOptionPane.showMessageDialog(null, "Categoría Eliminada Correctamente", "Ugallery", JOptionPane.INFORMATION_MESSAGE);
                    
                    usuarioActual.ImprimirCategorias();
                    nombreCategoriaTXT.setText(null);
                    nombreFotoTXT.setText(null);
                    fotoLBL.setIcon(null);
                    break;
                }
            }
            if(!existe){
                JOptionPane.showMessageDialog(null, "Esta Categoría No Existe.", "Ugallery", JOptionPane.ERROR_MESSAGE);
            }else{
                DefaultListModel<String> modelo3 = new DefaultListModel<>();
                for (Categoria categoria : usuarioActual.getListaCategoria()) {
                    modelo3.addElement(categoria.getNombreCategoria());
                }
                listaCategoriaLT.setModel(modelo3);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

