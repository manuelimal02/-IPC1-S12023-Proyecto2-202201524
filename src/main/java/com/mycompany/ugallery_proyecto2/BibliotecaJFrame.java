    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        Usuario UsuarioActual = (Usuario)AppState.listaUsuario.find(NombreUsuario);
        if(rutaTXT.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Seleccionar Una Imagen", "Ugallery", JOptionPane.ERROR_MESSAGE);
        }else if(listaCategoriaLT.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(null, "Seleccionar Una Categoría De La Imagen", "Ugallery", JOptionPane.ERROR_MESSAGE);
        }else{ 
            File archivo = new File(rutaTXT.getText());
            String fileName=archivo.getName();
                
            String nombre = FilenameUtils.getBaseName(fileName);
            ImageIcon imagen = new ImageIcon(rutaTXT.getText());
            LoteFoto nuevaImagen = new LoteFoto();
            nuevaImagen.setImagenIcon(imagen);
            nuevaImagen.setNombre(nombre);
            nuevaImagen.setRuta(rutaTXT.getText());
            
            Categoria categoria = (Categoria)UsuarioActual.getListaCategoria().get(listaCategoriaLT.getSelectedIndex());
            categoria.agregarImagen(nuevaImagen);
            
            JOptionPane.showMessageDialog(null, "Imagen agregada correctamente a la categoría: "+listaCategoriaLT.getSelectedValue(), "Ugallery", JOptionPane.INFORMATION_MESSAGE); 
            categoria.ImprimirImagenes();
            System.out.println("----------------------------------------------");

            rutaTXT.setText(null);
            nombreFotoTXT.setText(null);
            fotoLBL.setIcon(null);     
        }
    }//GEN-LAST:event_jButton8ActionPerformed

