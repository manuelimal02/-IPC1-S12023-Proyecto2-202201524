    private void listaCategoriaLTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaCategoriaLTMouseClicked
        // TODO add your handling code here:
        Usuario usuarioActual = (Usuario) AppState.listaUsuario.find(NombreUsuario); 
        Categoria categoria = (Categoria)usuarioActual.listaCategoria.get(listaCategoriaLT.getSelectedIndex());
        String NombreImagen;
        if(categoria!=null) {
            ListaDobleLoteFoto listaImagenes = categoria.getListaImagen();
            if (listaImagenes.get(0)!=null) {
                LoteFoto iconImagen = (LoteFoto) listaImagenes.get(0);
                NombreImagen =iconImagen.Nombre;
                iconoImagen= iconImagen.getImagenIcon();
                Image Imagen = iconoImagen.getImage();
                Imagen = Imagen.getScaledInstance(fotoLBL.getWidth(), fotoLBL.getHeight(), Image.SCALE_DEFAULT);
                fotoLBL.setIcon(new ImageIcon(Imagen));
                nombreFotoTXT.setText(NombreImagen);
            }else{
                nombreFotoTXT.setText(null);
                fotoLBL.setIcon(null); 
            }
        } 
    }//GEN-LAST:event_listaCategoriaLTMouseClicked

